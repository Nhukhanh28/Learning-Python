# Toán tử "not" trong Python
danh_sach = input ("Nhap danh sach: ")
if danh_sach:
    print ("Danh sach cua ban la: " + danh_sach)
if not danh_sach:
    print ("Ban chua nhap danh sach")

ten_tai_khoan = input("Nhap ten tai khoan: ")
if ten_tai_khoan: 
    print ("Ten tai khoan cua ban la: " + ten_tai_khoan)
if not ten_tai_khoan:
    print (" Vui long nhap ten tai khoan.")